
# Площадь прямоугольника
def S_rectangle(a, b):
    return a * b


# Площадь квадрата
def S_square(a):
    return a ** 2


# Площадь треугольника
def S_triangle(a, h):
    return 0.5 * a * h


# Площадь шара
def S_sphere(r):
    return round(3.14 * (r ** 2))


